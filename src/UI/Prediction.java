    private void btnPredictActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPredictActionPerformed
        
        txtMsg.setVisible(false);
        btnPredict.setText("Predicting...");
        btnPredict.disable();
        
        DBPrediction dbp=new DBPrediction();
        dbp.prediction();
        
        ArrayList<main.Prediction> pred=(ArrayList<main.Prediction>) PredictedList.getData();
        
        DefaultTableModel model=(DefaultTableModel)tblPrediction.getModel();
        model.setRowCount(0);
        Object[] row=new Object[3];
        
        for(int i=0;i<pred.size();i++)
                {

                    row[0]=pred.get(i).getPid();
                    row[1]=pred.get(i).getPname();
                    row[2]=pred.get(i).getPrediction();
 
                    model.addRow(row);
                }
        
        btnPredict.setText("Predict");
        btnPredict.enable(true);
    }//GEN-LAST:event_btnPredictActionPerformed

